exports.add = (a, b) => {
	console.log(`${a} + ${b} = ${a + b}`);
	return a + b;
};

exports.subtract = (a, b) => {
	console.log(`${a} - ${b} = ${a - b}`);
	return a - b;
};

exports.multiply = (a, b) => {
	console.log(`${a} * ${b} = ${a * b}`);
	return a * b;
};

exports.divide = (a, b) => {
	console.log(`${a} / ${b} = ${a / b}`);
	return a / b;
};
